#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*t;

	t = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			t = s;
		s++;
	}
	if (*s == c)
		return (char*)s;
	return ((char*)t);
}
